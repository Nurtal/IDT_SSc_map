def humanise_hub_list(hubs: list[dict]) -> str:
    """Pick up to 4 distinct hubs and give a clinician-readable rendering."""
    seen_modules: set[str] = set()
    out: list[str] = []
    for h in hubs:
        sp = h.get("species", "")
        mod = h.get("module", "?")
        base = sp.split("__", 1)[0]
        # Shorten complex names
        if len(base) > 35:
            base = base[:32] + "…"
        if mod in seen_modules and len(out) > 2:
            continue
        out.append(f"{base} ({mod})")
        seen_modules.add(mod)
        if len(out) >= 4:
            break
    return ", ".join(out)


def main(argv: list[str]) -> int:
    ap = argparse.ArgumentParser(description=__doc__.splitlines()[0])
    ap.add_argument(
        "--integrate-report",
        type=Path,
        default=Path("curation/celldesigner/SSc_MIM_integrated.report.json"),
    )
    ap.add_argument(
        "--network-summary",
        type=Path,
        default=Path("analysis/network/summary.json"),
    )
    ap.add_argument(
        "--reaction-tsv",
        type=Path,
        default=Path("curation/annotations/reaction_evidence.tsv"),
    )
    ap.add_argument(
        "--out",
        type=Path,
        default=Path("manuscript/ACR2026_late_breaking_abstract.md"),
    )
    args = ap.parse_args(argv[1:])

    if not args.integrate_report.exists():
        print("missing integration report; run `make integrate` first", file=__import__('sys').stderr)
        return 2

    integ = json.loads(args.integrate_report.read_text())
    netw = json.loads(args.network_summary.read_text()) if args.network_summary.exists() else {}

    # Count unique PMIDs from reaction_evidence.tsv
    pmids: set[str] = set()
    n_cross_module = 0
    if args.reaction_tsv.exists():
        lines = args.reaction_tsv.read_text().splitlines()[1:]
        for ln in lines:
            cells = ln.split("\t")
            if len(cells) > 4:
                for p in cells[4].split(";"):
                    p = p.strip()
                    if p:
                        pmids.add(p)
    # cross-module from integrate report dedupes
    n_cross_module = len(integ.get("dedupes", []))

    top_hubs = netw.get("top_hubs", [])[:6] if netw else []
    top_hubs_str = humanise_hub_list(top_hubs) or "[TO BE FILLED]"
    n_communities = (netw.get("communities") or {}).get("count", "[TBD]") if netw else "[TBD]"

    body = TEMPLATE.format(
        date=__import__("datetime").date.today().isoformat(),
        n_species=integ["totals"]["species"],
        n_reactions=integ["totals"]["reactions"],
        n_compartments=integ["totals"]["compartments"],
        n_pmids=len(pmids) if pmids else "[TBD]",
        n_cross_module=n_cross_module,
        top_hubs_str=top_hubs_str,
        n_communities=n_communities,
    )

    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text(body, encoding="utf-8")
    print(f"[ok] wrote {args.out}")
    print(f"     {integ['totals']['species']} species, {integ['totals']['reactions']} reactions, "
          f"{len(pmids)} PMIDs, {n_communities} communities")
    return 0


if __name__ == "__main__":
    raise SystemExit(main(__import__("sys").argv))
